def reject_edge_or_known_outlier_lines(
    line_abundances: np.ndarray,
    line_centers: np.ndarray,
    species_index: int,
    config: AutoSpecFitConfig,
) -> Tuple[np.ndarray, np.ndarray]:
    """Remove lines whose best-fit abundance falls at problematic grid edges."""
    valid = np.isfinite(line_abundances)

    if species_index == 0:
        # OH has a known low-abundance tail in this use case, so a slightly
        # higher lower bound is used.
        abundance_mask = (
            (line_abundances > config.oh_lower_rejection_limit)
            & (line_abundances < config.upper_rejection_limit)
        )
    else:
        abundance_mask = (
            (line_abundances > config.generic_lower_rejection_limit)
            & (line_abundances < config.upper_rejection_limit)
        )

    mask = valid & abundance_mask
    return line_abundances[mask], line_centers[mask]


def sigma_clip_line_abundances(abundances: np.ndarray, sigma_clipping: float) -> np.ndarray:
    """Apply conservative sigma clipping to line-by-line abundances.

    For small numbers of lines, no clipping is applied.  This follows the
    original ASF logic and avoids over-clipping species with few diagnostics.
    """
    abundances = np.asarray(abundances, dtype=float).ravel()
    abundances = abundances[np.isfinite(abundances)]

    if len(abundances) <= 1:
        return abundances
    if len(np.unique(abundances)) == 1:
        return abundances
    if len(abundances) < 11:
        return abundances

    mean = np.mean(abundances)
    std = np.std(abundances, ddof=1)
    if not np.isfinite(std) or std == 0:
        return abundances

    keep = np.abs(abundances - mean) < sigma_clipping * std
    return abundances[keep]


def median_distance_weighted_mean(abundances: np.ndarray) -> float:
    """Return the ASF median-distance weighted mean abundance.

    Lines closer to the median abundance receive higher weight.  The distance
    from the median is binned in units of 0.1 times the standard deviation,
    reproducing the weighting philosophy of the original ASF script.
    """
    abundances = np.asarray(abundances, dtype=float).ravel()
    abundances = abundances[np.isfinite(abundances)]

    if len(abundances) == 0:
        return np.nan
    if len(abundances) == 1:
        return float(abundances[0])

    std = np.std(abundances, ddof=1)
    median = np.median(abundances)
    distances = np.abs(abundances - median)

    if not np.isfinite(std) or std == 0:
        weights = np.ones_like(abundances)
    else:
        distance_bins = np.full_like(abundances, fill_value=5.0, dtype=float)
        for idx, distance in enumerate(distances):
            for bin_index in range(45):
                lower = bin_index * 0.1 * std
                upper = (bin_index + 1) * 0.1 * std
                if lower <= distance < upper:
                    distance_bins[idx] = (bin_index + 1) * 0.1
                    break
        weights = 1.0 / distance_bins

    return float(np.sum(abundances * weights) / np.sum(weights))


def combine_species_line_abundances(
    line_results: List[LineFitResult],
    species_index: int,
    config: AutoSpecFitConfig,
) -> Tuple[np.ndarray, float, float]:
    """Combine line-by-line abundances into a species-level abundance."""
    line_abundances = np.asarray([result.best_abundance_grid for result in line_results], dtype=float)
    line_centers = np.asarray([result.line_center for result in line_results], dtype=float)

    abundance_filtered, _line_centers_filtered = reject_edge_or_known_outlier_lines(
        line_abundances,
        line_centers,
        species_index,
        config,
    )
    clipped = sigma_clip_line_abundances(abundance_filtered, config.sigma_clipping)
    mean = median_distance_weighted_mean(clipped)
    rounded_mean = round(mean, 3) if np.isfinite(mean) else np.nan
    return clipped, mean, rounded_mean


def fit_species_in_iteration(
    iteration_id: int,
    species_index: int,
    species: SpeciesConfig,
    line_list: LineList,
    species_model_paths: List[Path],
    lam_star: np.ndarray,
    flux_star: np.ndarray,
    err_flux_star: np.ndarray,
    config: AutoSpecFitConfig,
    abundance_grid: np.ndarray,
    log_handle=None,
) -> SpeciesIterationResult:
    """Fit all selected lines for one species in one iteration.

    If a log file handle is provided, each line abundance is written
    immediately after it is measured. This reproduces the behavior of
    the original ASF script and allows progress to be monitored during
    long abundance iterations.
    """
    species_name = species.species_names[species_index]
    element_name = species.element_names[species_index]
    LOGGER.info("Iteration %d | fitting %s (%d lines)", iteration_id, species_name, line_list.n_lines)

    if log_handle is not None:
        log_handle.write(f"{element_name}\n")
        log_handle.flush()

    line_results: List[LineFitResult] = []

    for line_index in range(line_list.n_lines):
        chi2_curve, _valid_abundances = compute_line_chi2_curve(
            line_list=line_list,
            line_index=line_index,
            model_paths=species_model_paths,
            abundances=abundance_grid,
            lam_star=lam_star,
            flux_star=flux_star,
            err_flux_star=err_flux_star,
            config=config,
        )

        best_grid = best_grid_abundance(abundance_grid, chi2_curve)
        best_parabola, abundance_error = estimate_parabolic_chi2_uncertainty(
            abundance_grid,
            chi2_curve,
            fit_half_width=config.parabolic_fit_half_width,
        )

        line_result = LineFitResult(
            species_name=species_name,
            line_index=line_index,
            line_center=float(line_list.line_centers[line_index]),
            best_abundance_grid=best_grid,
            best_abundance_parabola=best_parabola,
            abundance_error=abundance_error,
            chi2_curve=chi2_curve,
        )
        line_results.append(line_result)

        # Write this line immediately, rather than waiting for the full
        # iteration to finish. This matches the older/original ASF behavior.
        if log_handle is not None:
            log_handle.write(
                f"{line_result.line_center:.6f} "
                f"{line_result.best_abundance_parabola:.6f} "
                f"{line_result.best_abundance_grid:.6f}\n\n"
            )
            log_handle.flush()

    clipped, mean, rounded_mean = combine_species_line_abundances(line_results, species_index, config)
    LOGGER.info(
        "Iteration %d | %s mean abundance = %.3f from %d clipped/accepted lines",
        iteration_id,
        species_name,
        rounded_mean,
        len(clipped),
    )

    return SpeciesIterationResult(
        species_name=species_name,
        element_name=element_name,
        line_results=line_results,
        clipped_line_abundances=clipped,
        mean_abundance=mean,
        rounded_mean_abundance=rounded_mean,
    )



def run_iteration(
    iteration_id: int,
    model_paths: List[List[Path]],
    species: SpeciesConfig,
    line_lists: List[LineList],
    lam_star: np.ndarray,
    flux_star: np.ndarray,
    err_flux_star: np.ndarray,
    config: AutoSpecFitConfig,
    abundance_grid: np.ndarray,
    turbospectrum_commands: Optional[List[str]] = None,
    log_handle=None,
) -> List[SpeciesIterationResult]:
    """Run one complete ASF abundance iteration.

    The iteration can optionally submit Turbospectrum jobs, wait for the
    expected synthetic spectra, fit all selected lines for all species, write
    diagnostic output files, and return the species-level abundance results.
    """
    run_ts_this_iteration = config.run_ts_by_iteration.get(iteration_id, config.run_turbospectrum)

    if run_ts_this_iteration:
        if config.turbospectrum_runner is None:
            raise ValueError(
                "config.run_turbospectrum=True, but config.turbospectrum_runner is not defined."
            )
        if not turbospectrum_commands:
            raise ValueError(
                "config.run_turbospectrum=True, but no Turbospectrum command strings were provided."
            )

        for command in turbospectrum_commands:
            run_turbospectrum_command(
                command=command,
                runner=config.turbospectrum_runner,
                execution_prefix=config.turbospectrum_execution_prefix,
            )
    else:
        LOGGER.info(
            "Skipping Turbospectrum launch for iteration %d; using existing synthetic spectra.",
            iteration_id,
        )

    wait_for_model_files(model_paths, config)

    if log_handle is not None:
        if iteration_id > 1:
            log_handle.write("--------------------------------------\n")
        log_handle.write(f"Iteration {iteration_id}\n")
        log_handle.write("*******************\n\n")
        log_handle.flush()

    species_results: List[SpeciesIterationResult] = []
    for species_index in range(species.n_species):
        result = fit_species_in_iteration(
            iteration_id=iteration_id,
            species_index=species_index,
            species=species,
            line_list=line_lists[species_index],
            species_model_paths=model_paths[species_index],
            lam_star=lam_star,
            flux_star=flux_star,
            err_flux_star=err_flux_star,
            config=config,
            abundance_grid=abundance_grid,
            log_handle=log_handle,
        )
        species_results.append(result)

    all_line_results = [line for result in species_results for line in result.line_results]
    write_iteration_chi2_table(iteration_id, abundance_grid, all_line_results, config.output_dir)
    write_iteration_abundance_error_table(iteration_id, all_line_results, config.output_dir)

    mean_abundances = np.asarray([result.rounded_mean_abundance for result in species_results], dtype=float)
    write_species_mean_table(iteration_id, species, mean_abundances, config)

    return species_results


def convergence_rule_for_iteration(
    iteration_id: int,
    config: AutoSpecFitConfig,
) -> Tuple[float, int, str]:
    """Return the convergence tolerance and allowed non-converged species count.

    The convergence rule becomes progressively more flexible with iteration
    number. Early iterations require all species to converge tightly. Later
    iterations allow one species to remain non-converged, in which case that
    species is treated as oscillating and its final abundance is estimated
    from its late-iteration history.
    """
    if iteration_id < config.intermediate_convergence_start_iteration:
        return config.early_convergence_tolerance, 0, "strict early convergence"

    if iteration_id < config.late_convergence_start_iteration:
        return config.intermediate_convergence_tolerance, 1, "intermediate convergence"

    return config.late_convergence_tolerance, 1, "late convergence"


def evaluate_convergence_status(
    change: np.ndarray,
    iteration_id: int,
    config: AutoSpecFitConfig,
) -> Tuple[bool, List[int], float, str]:
    """Evaluate ASF convergence for the current iteration.

    Returns
    -------
    converged
        True when the current iteration satisfies the tiered convergence rule.
    non_converged_indices
        Indices of species that do not satisfy the active convergence
        tolerance.
    active_tolerance
        Convergence tolerance used for this iteration.
    convergence_mode
        Text label describing the active convergence regime.
    """
    finite = np.isfinite(change)
    if not np.all(finite):
        active_tolerance, _max_non_converged, convergence_mode = (
            convergence_rule_for_iteration(iteration_id, config)
        )
        return False, [], active_tolerance, convergence_mode

    active_tolerance, max_non_converged, convergence_mode = (
        convergence_rule_for_iteration(iteration_id, config)
    )

    converged_mask = change <= active_tolerance
    non_converged_indices = np.where(~converged_mask)[0].astype(int).tolist()
    converged = len(non_converged_indices) <= max_non_converged

    return converged, non_converged_indices, active_tolerance, convergence_mode


def late_history_median(
    history: np.ndarray,
    window: int,
) -> Tuple[float, np.ndarray]:
    """Return the median abundance from the final finite iteration values.

    This statistic is used for species that do not satisfy the active
    convergence tolerance but are treated as oscillating or not fully
    converged. Only the final ``window`` finite iteration abundances are used,
    so early iterations do not influence the adopted final value.
    """
    history = np.asarray(history, dtype=float)
    finite_history = history[np.isfinite(history)]

    if len(finite_history) == 0:
        return np.nan, finite_history

    late_values = finite_history[-window:]
    return float(np.median(late_values)), late_values


def apply_late_history_statistics(
    final_not_rounded: np.ndarray,
    final_rounded: np.ndarray,
    indices: List[int],
    mean_history: np.ndarray,
    iteration_id: int,
    species: SpeciesConfig,
    config: AutoSpecFitConfig,
    log_handle,
    notes: List[str],
    context: str,
) -> None:
    """Apply late-history median statistics to selected species.

    Parameters
    ----------
    final_not_rounded, final_rounded
        Final abundance arrays updated in place.
    indices
        Species indices for which the final abundance should be replaced by
        the median of the final finite iteration values.
    mean_history
        Species-by-iteration abundance history.
    iteration_id
        Current ASF iteration number.
    context
        Short description written to the log file explaining why the
        late-history statistic was used.
    """
    for species_index in indices:
        element_name = species.element_names[species_index]
        adopted_value, late_values = late_history_median(
            mean_history[species_index, :iteration_id],
            config.final_statistics_window,
        )

        if np.isfinite(adopted_value):
            final_not_rounded[species_index] = adopted_value
            final_rounded[species_index] = np.round(adopted_value, 2)

            late_values_text = ", ".join(f"{value:+.3f}" for value in late_values)
            note = (
                f"Iteration {iteration_id}: {element_name} {context}; final "
                f"{element_name} abundance set to the median of the final "
                f"{len(late_values)} finite iteration values "
                f"({late_values_text}; median = {adopted_value:+.3f})."
            )
        else:
            note = (
                f"Iteration {iteration_id}: {element_name} {context}, but no "
                f"finite abundance history was available for replacement."
            )

        LOGGER.warning(note)
        log_handle.write(f"# {note}\n")
        log_handle.flush()
        notes.append(note)


