def read_observed_spectrum(path: Path, wavelength_scale: float) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Read observed wavelength, flux, and flux-error arrays.

    Parameters
    ----------
    path
        Text file with three columns: wavelength, flux, and flux error.
    wavelength_scale
        Multiplicative factor applied to the wavelength column.  Use 1e4 to
        convert microns to Angstrom.
    """
    path = Path(path)
    if not path.exists():
        raise FileNotFoundError(f"Observed spectrum file not found: {path}")

    data = np.loadtxt(path)
    if data.ndim != 2 or data.shape[1] < 3:
        raise ValueError(f"Observed spectrum must have at least 3 columns: {path}")

    lam_star = data[:, 0] * wavelength_scale
    flux_star = data[:, 1]
    err_flux_star = data[:, 2]
    return lam_star, flux_star, err_flux_star


def read_species_line_file(path: Path) -> LineList:
    """Read one species line-list file.

    Expected columns after the header line are:
    species_label, line_center, radial_velocity, fit_min_wavelength,
    fit_max_wavelength.
    """
    path = Path(path)
    if not path.exists():
        raise FileNotFoundError(f"Species line file not found: {path}")

    table = pd.read_csv(path, delim_whitespace=True, comment="#", skiprows=1, header=None)
    if table.shape[1] < 5:
        raise ValueError(f"Species file must contain at least 5 columns: {path}")

    return LineList(
        species_labels=table.iloc[:, 0].astype(str).tolist(),
        line_centers=table.iloc[:, 1].astype(float).to_numpy(),
        radial_velocities=table.iloc[:, 2].astype(float).to_numpy(),
        fit_min_wavelengths=table.iloc[:, 3].astype(float).to_numpy(),
        fit_max_wavelengths=table.iloc[:, 4].astype(float).to_numpy(),
    )


def read_all_line_lists(config: AutoSpecFitConfig, species: SpeciesConfig) -> List[LineList]:
    """Read line-list files for all species defined in ``SpeciesConfig``."""
    line_lists: List[LineList] = []
    for species_name in species.species_names:
        filename = f"{config.species_file_prefix}{species_name}{config.species_file_suffix}"
        line_lists.append(read_species_line_file(Path(filename)))
    return line_lists


def read_model_spectrum(path: Path, gaussian_sigma_pixels: float) -> Tuple[np.ndarray, np.ndarray]:
    """Read and smooth a synthetic spectrum.

    The synthetic spectrum is expected to contain at least two columns:
    wavelength and flux. The synthetic spectra generated by Turbospectrum
    should preferably be continuum-normalized before being analyzed with ASF.
    The model flux is smoothed to approximately match the resolution and
    sampling of the observed spectrum before fitting.
    """
    path = Path(path)
    if not path.exists():
        raise FileNotFoundError(f"Synthetic spectrum not found: {path}")

    data = np.loadtxt(path)
    if data.ndim != 2 or data.shape[1] < 2:
        raise ValueError(f"Synthetic spectrum must have at least 2 columns: {path}")

    lam_model = data[:, 0]
    flux_model = gaussian_filter1d(data[:, 1], sigma=gaussian_sigma_pixels, mode="nearest")
    return lam_model, flux_model


def write_iteration_chi2_table(
    iteration_id: int,
    abundances: np.ndarray,
    line_results: List[LineFitResult],
    output_dir: Path,
) -> None:
    """Save chi-square curves for all fitted lines in one iteration.

    The output has one row per tested abundance and one column per spectral
    line. Example columns are: abundance, OH_1, OH_2, Mg_1, Fe_1.
    Failed or skipped abundance points are written as NaN.
    """
    if not line_results:
        LOGGER.warning("No chi-square curves were saved for iteration %s.", iteration_id)
        return

    chi2_columns = [result.chi2_curve for result in line_results]
    line_labels = [result.label for result in line_results]
    table = pd.DataFrame(
        np.column_stack([abundances, *chi2_columns]),
        columns=["abundance", *line_labels],
    )

    output_path = Path(output_dir) / f"Chi2_Iteration_{iteration_id}.txt"
    table.to_csv(output_path, sep=" ", index=False, float_format="%.6f", na_rep="nan")
    LOGGER.info("Wrote chi-square table: %s", output_path)


def write_iteration_abundance_error_table(
    iteration_id: int,
    line_results: List[LineFitResult],
    output_dir: Path,
) -> None:
    """Save line-by-line abundance uncertainties for one iteration.

    Uncertainties are estimated from local parabolic fits to chi-square as a
    function of abundance using the criterion chi2 = chi2_min + 1.
    """
    if not line_results:
        LOGGER.warning("No abundance errors were saved for iteration %s.", iteration_id)
        return

    table = pd.DataFrame(
        [[result.abundance_error for result in line_results]],
        columns=[result.label for result in line_results],
    )
    output_path = Path(output_dir) / f"Abundance_Errors_Iteration_{iteration_id}.txt"
    table.to_csv(output_path, sep=" ", index=False, float_format="%.6f", na_rep="nan")
    LOGGER.info("Wrote abundance-error table: %s", output_path)


