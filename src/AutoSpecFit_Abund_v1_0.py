# Chi-square fitting and abundance-uncertainty estimation
# -----------------------------------------------------------------------------

def estimate_parabolic_chi2_uncertainty(
    abundances: np.ndarray,
    chi2_values: np.ndarray,
    fit_half_width: float = 0.10,
) -> Tuple[float, float]:
    """Estimate best abundance and uncertainty from a local parabolic fit.

    A quadratic function is fitted to chi-square values within
    ``sampled_minimum +/- fit_half_width``.  The 1-sigma error is estimated from
    ``chi2 = chi2_min + 1``.

    Returns ``(nan, nan)`` if the fit cannot be estimated reliably.
    """
    x = np.asarray(abundances, dtype=float)
    y = np.asarray(chi2_values, dtype=float)

    valid = np.isfinite(x) & np.isfinite(y)
    x = x[valid]
    y = y[valid]

    if len(x) < 3:
        return np.nan, np.nan

    order = np.argsort(x)
    x = x[order]
    y = y[order]

    sampled_minimum_index = int(np.argmin(y))
    sampled_best_abundance = x[sampled_minimum_index]

    local = np.abs(x - sampled_best_abundance) <= fit_half_width
    x_fit = x[local]
    y_fit = y[local]

    if len(x_fit) < 3:
        return sampled_best_abundance, np.nan

    try:
        a, b, c = np.polyfit(x_fit, y_fit, 2)
    except (np.linalg.LinAlgError, ValueError):
        return sampled_best_abundance, np.nan

    # A meaningful chi-square parabola must open upward.
    if not np.isfinite(a) or a <= 0:
        return sampled_best_abundance, np.nan

    best_abundance = -b / (2.0 * a)

    # Reject a vertex that lies outside the local fitting window.
    if best_abundance < np.min(x_fit) or best_abundance > np.max(x_fit):
        best_abundance = sampled_best_abundance
        chi2_min = y[sampled_minimum_index]
    else:
        chi2_min = a * best_abundance**2 + b * best_abundance + c

    # Solve the quadratic equation for chi2 = chi2_min + 1.
    roots = np.roots([a, b, c - (chi2_min + 1.0)])
    roots = np.real(roots[np.isreal(roots)])
    roots = np.sort(roots)

    if len(roots) < 2:
        return best_abundance, np.nan

    left_roots = roots[roots <= best_abundance]
    right_roots = roots[roots >= best_abundance]
    if len(left_roots) == 0 or len(right_roots) == 0:
        return best_abundance, np.nan

    abundance_error = 0.5 * (right_roots[0] - left_roots[-1])
    if not np.isfinite(abundance_error) or abundance_error <= 0:
        abundance_error = np.nan

    return best_abundance, abundance_error


def compute_line_chi2_curve(
    line_list: LineList,
    line_index: int,
    model_paths: List[Path],
    abundances: np.ndarray,
    lam_star: np.ndarray,
    flux_star: np.ndarray,
    err_flux_star: np.ndarray,
    config: AutoSpecFitConfig,
) -> Tuple[np.ndarray, np.ndarray]:
    """Compute chi-square values for one line over the abundance grid.

    Returns
    -------
    chi2_curve
        Array with one value per tested abundance.  Failed normalization/model
        points remain NaN.
    valid_abundances
        Abundance values for which a valid chi-square value was measured.
    """
    chi2_curve = np.full(len(abundances), np.nan)
    valid_abundances = []

    line_center = line_list.line_centers[line_index]
    radial_velocity = line_list.radial_velocities[line_index]
    fit_min = line_list.fit_min_wavelengths[line_index]
    fit_max = line_list.fit_max_wavelengths[line_index]

    for abundance_index, model_path in enumerate(model_paths):
        try:
            lam_model, flux_model = read_model_spectrum(model_path, config.gaussian_sigma_pixels)
        except FileNotFoundError:
            LOGGER.warning("Skipping missing model: %s", model_path)
            continue

        """AutoSpecNorm performs local normalization and returns the normalized
        observed flux, observed flux uncertainty normalized in the same way as 
        the observed flux, interpolated synthetic flux,  and the normalization
        uncertainty (delta).
        """
        (
            lam_star_cut,
            _flux_star_cut,
            flux_interp_model_cut,
            flux_star_cut_normalized,
            _upper_flux_star_cut_normalized,
            _lower_flux_star_cut_normalized,
            err_flux_star_cut_normalized,
            lam_cut_work,
            _flux_star_cut_work,
            _flux_interp_model_cut_work,
            delta,
            flag,
        ) = AutoSpecNorm_Regions(
            line_center,
            config.lam_diff_large,
            radial_velocity,
            lam_star,
            flux_star,
            err_flux_star,
            lam_model,
            flux_model,
            config.limit_model,
            config.limit_star,
            config.peak_index_model,
            config.peak_index_star,
            config.autospecnorm_iteration_number,
        )

        # Reject cases where AutoSpecNorm could not identify usable continuum
        # points on both sides of the fitted line.
        has_left_point = np.any(lam_cut_work < line_center)
        has_right_point = np.any(lam_cut_work > line_center)
        if not has_left_point or not has_right_point or flag == 0:
            continue

        fit_mask = (lam_star_cut >= fit_min) & (lam_star_cut <= fit_max)
        if not np.any(fit_mask):
            continue

        obs = flux_star_cut_normalized[fit_mask]
        obs_err = err_flux_star_cut_normalized[fit_mask]
        model = flux_interp_model_cut[fit_mask]
        delta_fit = delta[fit_mask]

        # Chi-square includes both the observational flux uncertainty and the
        # normalization uncertainty returned by AutoSpecNorm.
        variance = obs_err**2 + delta_fit**2
        valid_variance = variance > 0
        if not np.any(valid_variance):
            continue

        chi2_value = np.sum(((obs[valid_variance] - model[valid_variance]) ** 2) / variance[valid_variance])
        chi2_curve[abundance_index] = chi2_value
        valid_abundances.append(abundances[abundance_index])

    return chi2_curve, np.asarray(valid_abundances, dtype=float)


def best_grid_abundance(abundances: np.ndarray, chi2_curve: np.ndarray) -> float:
    """Return the grid abundance corresponding to the minimum chi-square."""
    valid = np.isfinite(chi2_curve)
    if not np.any(valid):
        return np.nan
    valid_indices = np.where(valid)[0]
    best_index = valid_indices[int(np.argmin(chi2_curve[valid]))]
    return float(abundances[best_index])


