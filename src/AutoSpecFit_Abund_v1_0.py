def build_model_filename(
    stellar_parameters: StellarParameters,
    element_abundances: Dict[str, str],
    model_extension: str,
) -> str:
    """Build one expected Turbospectrum synthetic-spectrum filename.

    Parameters
    ----------
    stellar_parameters
        Fixed stellar parameters used in the model filename stem.
    element_abundances
        Mapping from element code, e.g. ``x26``, to abundance string, e.g.
        ``+0.120``.  The order is preserved by normal Python dictionaries in
        Python 3.7+; the caller should construct the dictionary in the desired
        filename order.
    model_extension
        Filename extension, usually ``.mod``.
    """
    stem = stellar_parameters.base_model_stem()
    abundance_part = "".join(f"_{code}{abundance}" for code, abundance in element_abundances.items())
    return f"{stem}{abundance_part}{model_extension}"


def first_iteration_model_paths(
    config: AutoSpecFitConfig,
    stellar_parameters: StellarParameters,
    species: SpeciesConfig,
) -> List[List[Path]]:
    """Return model paths for iteration 1.

    In iteration 1, only one element abundance varies in each synthetic grid.
    """
    abundance_strings = config.abundance_strings()
    all_paths: List[List[Path]] = []
    for element_code in species.element_codes:
        species_paths = []
        for abundance in abundance_strings:
            filename = build_model_filename(
                stellar_parameters,
                {element_code: abundance},
                config.model_extension,
            )
            species_paths.append(Path(config.model_dir) / filename)
        all_paths.append(species_paths)
    return all_paths


def followup_iteration_model_paths(
    config: AutoSpecFitConfig,
    stellar_parameters: StellarParameters,
    species: SpeciesConfig,
    previous_abundance_strings: List[str],
) -> List[List[Path]]:
    """Return model paths for iteration >= 2.

    For a target species, its abundance is varied over the grid while the
    abundances of all other species are fixed to the previous iteration values.

    Important
    ---------
    The filename order intentionally follows the original ASF code: all fixed
    elements are appended first, in species-list order, and the target/variable
    element is appended last.  This order must match the Turbospectrum parallel
    command generated by ``followup_iteration_turbospectrum_commands``; otherwise
    ASF would wait for filenames that differ from the files generated by the
    synthesis script.
    """
    abundance_strings = config.abundance_strings()
    all_paths: List[List[Path]] = []

    for target_idx, target_code in enumerate(species.element_codes):
        species_paths = []
        for trial_abundance in abundance_strings:
            abundance_map: Dict[str, str] = {}

            # Append all fixed abundances from the previous iteration first.
            for idx, code in enumerate(species.element_codes):
                if idx != target_idx:
                    abundance_map[code] = previous_abundance_strings[idx]

            # Append the target element with the trial abundance last.
            abundance_map[target_code] = trial_abundance

            filename = build_model_filename(stellar_parameters, abundance_map, config.model_extension)
            species_paths.append(Path(config.model_dir) / filename)
        all_paths.append(species_paths)

    return all_paths


def build_turbospectrum_parallel_string(
    stellar_parameters: StellarParameters,
    variable_element_code: str,
    variable_abundance_strings: List[str],
    fixed_element_abundances: Optional[Dict[str, str]] = None,
) -> str:
    """Build the GNU-parallel argument string passed to the TS script.

    This function intentionally reproduces the string construction used in
    the original ASF script.  The exact underscore placement and ``:::``
    spacing are preserved because the external Turbospectrum bash script and
    GNU parallel command expect this format.

    Original ASF pattern for iteration 1:

        starting_str + space + e_str + middle_str + space_new + ' ' + abund_str_long

    Original ASF pattern for later iterations:

        starting_str + middle_str_new + space_new + ' ' + abund_str_long

    In both cases, the variable element is appended last and receives the full
    abundance grid.  Any fixed elements from the previous ASF iteration are
    inserted before the variable element.
    """
    fixed_element_abundances = fixed_element_abundances or {}

    # Preserve the original ASF command stem used by the external
    # Turbospectrum execution script.
    starting_str = "  t{1}_g{2}_z{3}_a{4}_v{5}"

    # Preserve the original GNU-parallel structure while using the current
    # stellar-parameter inputs.
    middle_str_new = (
        f" ::: {stellar_parameters.teff}"
        f" ::: {stellar_parameters.logg}"
        f" ::: {stellar_parameters.metallicity}"
        f" ::: {stellar_parameters.alpha}"
        f" ::: {stellar_parameters.vmic}"
    )

    space = "_"
    space_new = " ::: "
    curl_o = "{"
    curl_c = "}"

    placeholder_index = 6

    # Add fixed elements from the previous ASF iteration in the same order
    # passed by the caller.  This matches the original loop over q != p.
    for element_code, abundance_string in fixed_element_abundances.items():
        starting_str = (
            starting_str
            + space
            + element_code
            + curl_o
            + str(placeholder_index)
            + curl_c
        )

        # The extra blank after ":::" is intentional and follows the original
        # ASF command format expected by the external bash script.
        middle_str_new = middle_str_new + space_new + " " + abundance_string
        placeholder_index += 1

    # Add the variable element last, following the original ASF command
    # format expected by the external bash script.
    starting_str = (
        starting_str
        + space
        + variable_element_code
        + curl_o
        + str(placeholder_index)
        + curl_c
    )

    abund_str_long = " ".join(variable_abundance_strings)

    # Return the full GNU-parallel argument string passed to the external
    # Turbospectrum execution script.
    return starting_str + middle_str_new + space_new + " " + abund_str_long


def first_iteration_turbospectrum_commands(
    config: AutoSpecFitConfig,
    stellar_parameters: StellarParameters,
    species: SpeciesConfig,
) -> List[str]:
    """Build one Turbospectrum command string per target species for iteration 1."""
    abundance_strings = config.abundance_strings()
    return [
        build_turbospectrum_parallel_string(
            stellar_parameters=stellar_parameters,
            variable_element_code=element_code,
            variable_abundance_strings=abundance_strings,
        )
        for element_code in species.element_codes
    ]


def followup_iteration_turbospectrum_commands(
    config: AutoSpecFitConfig,
    stellar_parameters: StellarParameters,
    species: SpeciesConfig,
    previous_abundance_strings: List[str],
) -> List[str]:
    """Build one Turbospectrum command string per target species for iteration >= 2.

    For the target species, the abundance is varied over the abundance grid.
    All other species are fixed to the mean abundances inferred in the previous
    ASF iteration.
    """
    abundance_strings = config.abundance_strings()
    commands: List[str] = []

    for target_index, target_code in enumerate(species.element_codes):
        fixed_abundances: Dict[str, str] = {}
        for idx, element_code in enumerate(species.element_codes):
            if idx != target_index:
                fixed_abundances[element_code] = previous_abundance_strings[idx]

        commands.append(
            build_turbospectrum_parallel_string(
                stellar_parameters=stellar_parameters,
                variable_element_code=target_code,
                variable_abundance_strings=abundance_strings,
                fixed_element_abundances=fixed_abundances,
            )
        )

    return commands


def wait_for_model_files(model_paths: Iterable[Iterable[Path]], config: AutoSpecFitConfig) -> None:
    """Wait until all expected synthetic spectra exist on disk.

    This function allows ASF to pause after submitting Turbospectrum jobs and
    continue automatically once all required spectra have been generated.
    """
    if not config.wait_for_models:
        return

    flat_paths = [Path(path) for species_paths in model_paths for path in species_paths]

    for check_number in range(1, config.max_model_wait_checks + 1):
        missing = [path for path in flat_paths if not path.exists()]
        if not missing:
            LOGGER.info("All %d synthetic spectra are available.", len(flat_paths))
            return

        LOGGER.info(
            "Waiting for synthetic spectra: %d missing files at check %d/%d.",
            len(missing),
            check_number,
            config.max_model_wait_checks,
        )
        time.sleep(config.model_wait_seconds)

    missing_preview = "\n".join(str(path) for path in missing[:10])
    raise TimeoutError(
        "Synthetic spectra were not generated within the allowed wait time. "
        f"First missing files:\n{missing_preview}"
    )


def run_turbospectrum_command(command: str, runner: str, execution_prefix: str = "") -> None:
    """Submit one external Turbospectrum synthesis command.

    This preserves the original command structure:

        module load parallel; sbatch parallel ./run_TS_Interpolated_Model_H_band.sh <parallel string>

    The runner is kept as a string, not converted to ``Path`` for execution, so
    that a leading ``./`` is preserved exactly.
    """
    runner_path = Path(runner)
    if not runner_path.exists():
        raise FileNotFoundError(f"Turbospectrum execution script not found: {runner}")

    if execution_prefix:
        full_command = f"{execution_prefix} {runner} {command}"
    else:
        full_command = f"{runner} {command}"


    exit_status = os.system(full_command)
    if exit_status != 0:
        raise RuntimeError(
            f"Turbospectrum command failed with exit status {exit_status}:\n{full_command}"
        )


