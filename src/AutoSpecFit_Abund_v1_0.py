    teff: str = "3700"
    logg: str = "+4.5"
    metallicity: str = "+0.20"
    alpha: str = "+0.00"
    vmic: str = "1.00"

    def base_model_stem(self) -> str:
        """Return the common filename stem for all synthetic spectra."""
        return (
            f"t{self.teff}_g{self.logg}_z{self.metallicity}"
            f"_a{self.alpha}_v{self.vmic}"
        )

